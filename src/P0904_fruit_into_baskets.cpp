#include <common_headers.h>

class Solution
{
public:
	int totalFruit(vector<int>& fruits)
	{
		using fruit_tree_position = int;
		fruit_tree_position basket_1 {-1};
		fruit_tree_position basket_2 {-1};

		int result {0};
		int tree_num {static_cast<int>(fruits.size())};
		for (fruit_tree_position left = 0, right = left; right < tree_num; ++right)
		{// In every turn, pick the "fruit[right]"
			int picking_fruit { fruits[right]};
			if (basket_1 == -1 || fruits[basket_1] == picking_fruit)
			{
				basket_1 = right;
			}
			else if (basket_2 == -1 || fruits[basket_2] == picking_fruit)
			{
				basket_2 = right;
			}
			else if (picking_fruit != fruits[basket_1] && picking_fruit != fruits[basket_2])
			{// Not found
				if (basket_1 < basket_2)
				{
					left = basket_1 + 1;
					basket_1 = right;
				} else {
					left = basket_2 + 1;
					basket_2  = right;
				}
			}
			result = max(result, right - left + 1);
		}
		return result;
	}
};


class OlderSolution
{
public:
	[[maybe_unused]]int totalFruit(vector<int>& fruits)
	{
		using type_t = int;
		int max_sum{ 0 };
		auto left_window = fruits.begin();
		auto right_window = fruits.begin();
		int types_num{ 0 };

		while (right_window != fruits.end())
		{
			if (std::find(left_window, right_window, *right_window) == right_window)
			{// the fruit (on the right window position) not found in current window scope
				++types_num;
			}
			++right_window;

			if (types_num > 2)
			{
				// Get the newest 2 types from right to left
				vector<type_t> current_types;
				current_types.clear();
				for (auto iter = right_window - 1;; --iter)
				{
					if (std::find(current_types.begin(), current_types.end(), *iter) == current_types.end())
					{ //not found
						current_types.emplace_back(*iter);

						if (current_types.size() > 2)
						{
							left_window =
								iter + 1; // Because the *iter exceed 2 baskets, so let it +1 to exclude itself
							break;
						}
					}
				}
				types_num = 2;
			}
			max_sum = std::max(max_sum, static_cast<int>(right_window - left_window));
		}
		return max_sum;
	}
};

